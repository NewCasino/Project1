<li><strong> Jak mohu vložit na svůj účet?</strong>
<p>Klikněte na „Můj účet“ a poté tlačítko „Vložit peníze“ v levém sloupci, poté si vyberte patřičnou platební metodu.</p>
</li><li><strong> Jak dlouho trvá než je můj vklad schválen?</strong>
<p>U většiny platebních metod je Váš vklad potvrzen okamžitě. V případě vkladu z bankovního účtu ale může trvat až 7 dní než obdržíme z banky Váš vklad.</p>
</li><li><strong> Které platební metody máte k dispozici?</strong>
<p>Prosím klikněte na odkaz „Platební metody“ na horní liště všech [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] stránek a vyberte si platební metodu, která Vám bude vyhovovat.  </p>
</li><li><strong> Které kreditní/debitní karty přijímáte?</strong>
<p>[Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] přijímá všechny kreditní a debitní karty Visa, Mastercard/Maestro. S učitými omezeními se můžete setkat pouze při výběrech pomocí Mastercard/Maestro.</p>
</li><li><strong> Budete mi účtovat nějaké poplatky za vklad na [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] účet?</strong>
<p>Při vkladu na [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] účet Vám žádné poplatky účtovat nebudeme.</p>
</li><li><strong> Jaká je minimální/maximální částka na jeden vklad?</strong>
<p>Minimální vklad na Váš účet je €10 a jejich ekvivalent v jiných měnách a maximální vklad ekvivalent €5000 denně.</p>
</li><li><strong> Existuje způsob, jak si nastavit denní, týdenní nebo měsíční limity na vklad?</strong>
<p>Denní, týdenní nebo měsíční limity na vklad si můžete nastavit v sekci „Můj účet“ pod záložkou „Zodpovědné sázení“. Limit si můžete kdykoli změnit.</p>
</li><li><strong> Jak mohu učinit vklad z bankovního účtu?</strong>
<p>Klikněte prosím v sekci „Můj účet“ na „Vložit peníze“ a vyberte „Vklad bankou“. Možnost aktivujete kliknutím na „Vklad“ a následováním instrukcí na své obrazovce. </p>
