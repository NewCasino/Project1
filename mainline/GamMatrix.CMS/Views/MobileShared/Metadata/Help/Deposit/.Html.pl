<ol><li><strong> Jak dokonać wpłaty na konto?</strong>
<p>Kliknij przycisk „Moje konto”, a następnie „Wpłata” w kolumnie po lewej. Teraz możesz wybrać preferowany sposób płatności.</p>
</li><li><strong> Ile trwa przyjęcie mojej wpłaty?</strong>
<p>W przypadku większości sposobów płatności wpłata zostanie przyjęta natychmiast. Natomiast w przypadku przelewu bankowego otrzymanie środków przez bank może zająć nawet 7 dni. Po upływie tego czasu kwota wpłaty zostanie uznana na Twoim koncie [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)].</p>
</li><li><strong> Jakie sposoby płatności są dostępne w serwisie?</strong>
<p>Aby zapoznać się ze wszystkimi dostępnymi sposobami płatności kliknij przycisk „Sposoby płatności” w górnym menu na dowolnej podstronie serwisu [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)].</p>
</li><li><strong> Jakie karty kredytowe/debetowe honorujecie?</strong>
<p>Przy realizacji wpłat [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] honoruje wszystkie karty kredytowe i debetowe Visa i Mastercard/Maestro, chociaż w przypadku tych ostatnich obowiązują pewne ograniczenia dotyczące wypłat środków.</p>
</li><li><strong> Czy przy wpłacie na konto [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] zostaną naliczone opłaty?</strong>
<p>Przy wpłatach na konto [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] nie pobieramy żadnych opłat.</p>
</li><li><strong> Ile wynosi minimalna/maksymalna kwota wpłaty jednorazowej?</strong>
<p>Minimalna kwota wpłaty to 10 euro lub równowartość tej sumy w walucie krajowej, natomiast maksymalna wpłata dzienna wynosi 5 000 euro lub równowartość w walucie krajowej użytkownika.</p>
</li><li><strong> Czy mogę ustalić dzienny, tygodniowy i miesięczny limit wpłat?</strong>
<p>Tak, serwis umożliwia ustalanie limitów dziennych, tygodniowych i miesięcznych. Można je określić w sekcji „Moje konto”, podsekcja „Odpowiedzialne korzystanie z gier hazardowych – limit wpłat”. Limity można zmienić w dowolnej chwili w tej samej sekcji.</p>
</li><li><strong> Jak dokonać wpłaty na konto [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] z rachunku bankowego?</strong>
<p>Kliknij przycisk „Wpłata” w sekcji „Moje konto” i wybierz opcję „Przelew bankowy”. Opcję tę można aktywować klikając przycisk „Wpłata”. Następnie wykonuj polecenia na ekranie.</p>
</li><li><strong> Czy istnieje limit tygodniowy wpłat kartą kredytową no moje konto w serwisie?</strong>
<p>Maksymalna kwota wpłat dziennych wynosi 5 000 euro. </p>
</li>
</ol>

