<ol>
<li><strong>Pobrałem oprogramowanie - gdzie zostało zapisane?</strong>
<p>Po pomyślnym zainstalowaniu oprogramowania [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] Poker na komputerze, na pulpicie pojawi się ikona [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] Poker. Wystarczy kliknąć w nią dwa razy, aby uruchomić program do gry w pokera i zostać przekierowanym do głównego lobby dla graczy.</p>
</li>

<li><strong>Dlaczego moje hasło zostało odrzucone?</strong>
<p>Wpisanie błędnego hasła często powoduje wyświetlenie komunikatu "Nie można zweryfikować hasła". Sprawdź, czy masz wciśnięty klawisz Caps Lock, ponieważ przy wprowadzaniu hasła liczy się wielkość znaków. Jeśli problem nadal występuje, naciśnij przycisk "Zapomniałem hasła". Nowe hasło zostanie wysłane na adres email przypisany do Twojego konta [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)].</p>
</li>

<li><strong>Jak mogę zmienić nick?</strong>
<p>Program [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] Poker umożliwia graczom zmianę identyfikatora (czyli imienia, pod jakim figurują przy karcianym stoliku) co 7 dni. W tym celu po zalogowaniu do programu [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] Poker otwórz zakładkę "Moje konto".</p>
</li>
</ol>

