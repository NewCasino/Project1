<ol><li><strong> Jaké jsou minimální a maximální povolené sázky?</strong>
<p>Maximální sázky se liší podle události. Pokud se pokusíte uzavřít sázku větší, než je maximální limit, dostanete zprávu, která vám oznámí maximální částku, kterou na tuto položku smíte vsadit. Nemáme limit na minimální sázku.</p>
</li><li><strong> Co jsou jednoduché sázky a vícenásobné sázky?</strong>
<p>Jednoduché sázky obsahují pouze jednu položku, vícenásobné sázky vyžadují více než jednu. Čím více položek spojíte dohromady do vícenásobné sázky (např. všechny domácí týmy v kole Premier League), tím více můžete vyhrát, ale samozřejmě je to o moc těžší!</p>
</li><li><strong> Kolik položek může vícenásobná sázka obsahovat?</strong>
<p>Maximální počet položek, které připouštíme ve vícenásobných sázkách, je 10.</p>
</li><li><strong> Jak funguje sázení s hendikepem?</strong>
<p>Podrobné vysvětlení sázení s hendikepem (1×2 s hendikepem nebo asijskými hendikepy) naleznete v části Vysvětlení sázek zde.</p>
</li><li><strong> Co se stane, když je zápas odložen?</strong>
<p>Podívejte se na pravidla daného sportu. U některých sportů musí být zápas odehrán týž den, aby sázky platily, sázky v jiných sportech mohou zůstat otevřené do doby, než se zápas dohraje nebo oficiálně předčasně ukončí.</p>
</li><li><strong> Proč mi ještě nebyla vyplacena výherní sázka?</strong>
<p>[Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] vyžaduje, aby byly vyhlášeny oficiální výsledky, než vypořádá sázky. Někdy mohou být online zdroje jako livescore weby nepřesné, takže vypořádání se může zdržet do doby, než jsou výsledky potvrzeny.</p>
</li><li><strong> Změnil jsem názor, mohu zrušit svoji sázku?</strong>
<p>Ne. Poté co jste sázku potvrdili a ta byla naším serverem přijata, nelze sázku zrušit.</p>
</li><li><strong> Kde najdu záznam svých sázek?</strong>
<p>Historii svého sázení a transakcí najdete v části „Můj účet pro sázení na sport“, když jste přihlášeni na webu [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)].</p>
</li><li><strong> Proč jsou výhry v britských kurzech počítány jinak?</strong>
<p>Při počítání výher používáme desítkové (EU) kurzy, protože jsou přesnější. Britské a americké kurzy jsou uvedeny jen z informativních důvodů, protože desítkové kurzy ne vždy přesně odpovídají jednoduchému zlomku.</p>
</li><li><strong> Začínám být na sázení závislý. Co mohu dělat?</strong>
<p>Sázení se společností [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] by pro každého mělo představovat příjemně strávený volný čas, ale pro malou menšinu hráčů se může stát závislostí a způsobovat problémy. Informace o závislosti na hazardu najdete prostřednictvím části Zodpovědné hraní. </p>
</li>
