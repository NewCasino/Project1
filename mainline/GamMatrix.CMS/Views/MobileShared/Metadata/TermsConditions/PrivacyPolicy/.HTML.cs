<strong>Ochrana soukromí</strong>
<p>Užíváním a/nebo návštěvou stránek [Metadata:value(/Metadata/Settings.Operator_DisplayName)] (dále jen <b>„stránky“</b>); nebo otevřením účtu na stránkách se zavazujete k dodržování těchto zásad pro Ochranu soukromí. Při používání produktů stránek budete požádáni o pravdivé, aktuální a přesné osobní údaje, jejichž pomocí vás můžeme identifikovat. <br />
i) Zároveň povolujete zpracování svých osobních údajů podle těchto příslušných zákonů platných v dané oblasti. <br />
ii) Potvrzujete, že se společností dobrovolně sdílíte určité osobní údaje, které mohou sloužit k ověření vaší totožnosti. Tyto údaje získáváme v rámci svých přísných ověřovacích postupů, které předchází mezinárodnímu praní špinavých peněz a zajišťují dokonalou bezpečnost veškeré herní činnosti. <br />
Společnost se zavazuje chránit a respektovat vaše soukromí i zabezpečení a plně dodržovat příslušné zákony na ochranu dat. </p>
</li><br />
<li>
<strong>Získávání osobních údajů: </strong>
<p>
Tyto údaje získáváme, když se u nás registrujete a používáte naše herní produkty, máte dotazy, registrujete se pro různé nabídky nebo jiné služby, případně když odpovídáte na korespondenci (mimo jiné například v podobě dotazníků). Získávané informace mohou zahrnovat osobní a bankovní údaje, případně další informace, které nám předáte.</p> 
</li><br />
<li>
<strong>Jak jsou informace zpracovány</strong>
<p>
Informace, které nám předáte, se zpracovávají za řadou účelů, mimo jiné v rámci poskytnutí herních služeb, zpracování vašich dotazů či požadavků, poskytování příslušné podpory, marketingu, upozornění na změny, které můžeme na stránkách nebo u svých služeb čas od času provádět, dále za účelem plnění povinností určených regulačními orgány oblasti, ve které máme licenci, případně za jakýmkoli dalším specifickým účelem, v jehož rámci jste byli o informace požádáni. </p>
</li><br />
<li>
<strong>Komu osobní údaje smíme předat: </strong>
<p>
Vaše osobní údaje můžeme předávat:<br />
a)  společnostem skupiny. V tomto případě zajišťujeme, aby k nim měli přístup pouze autorizovaní zaměstnanci;<br />
b)  společnostem třetích stran, se kterými spolupracujeme při poskytování herních služeb;<br />
c)  společnostem třetích stran, které poskytují služby nám, například mimo jiné zpracovatelům plateb nebo agenturám pro ověřování dat;<br />
d)  konzultantům či auditorům, kteří nabízí své služby kterékoli společnosti skupiny za účelem zlepšování našich postupů;<br />
e)  potenciálním investorům nebo kupujícímu společnosti, která je součástí skupiny;<br />
f)  vládním úřadům nebo institucím, ovšem výhradně v případech, kdy to vyžaduje zákon.<br />
Všechny třetí strany, který osobní údaje sdělujeme, jsou smluvně zavázány dodržovat příslušné zákony a podmínky těchto zásad. </p>
