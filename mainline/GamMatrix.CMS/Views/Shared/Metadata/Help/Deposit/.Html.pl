<ol>

 <li> <strong>Jak dokonuję wpłaty na swoje konto?</strong>
 <p>
 Kliknij Moje konto, a następnie przycisk Wpłać w kolumnie po lewej stronie. Potem wybierasz preferowaną metodę płatności.
 </p>
 </li>

 <li> <strong>Jak długo trwa zatwierdzenie mojej wpłaty?</strong>
 <p>
 W przypadku większości metod płatności wpłata jest zatwierdzana natychmiast. W przypadku przelewu bankowego może minąć 7 dni zanim pieniądze dotrą z banku. Dopiero wtedy umieszczamy środki na Twoim [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] koncie.
 </p>
 </li>

 <li>
 <strong>Jakie metody wpłaty są dostępne?</strong>
 <p>
 Kliknij Metody płatności w górnym menu jakiejkolwiek [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] strony&nbsp;, aby wyświetlić dostępne dla siebie metody płatności.
 </p>
 </li>

 <li>
 <strong>Które karty kredytowe/debetowe akceptujecie?</strong>
 <p>
 [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] akceptujemy wszystkie karty kredytowe oraz debetowe Visa i MasterCard/Maestro, choć mogą istnieć ograniczenia dotyczące wypłat na kartę MasterCard/Maestro.
 </p>
 </li>

 <li>
 <strong>Czy zostaną mi naliczone jakiekolwiek opłaty z tytułu [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] wpłaty na konto?</strong> 
 <p>
 Nie ma żadnych opłat za dokonywanie wpłaty na swoje [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] konto.
 </p>
 </li>

 <li>
 <strong>Jakie są minimalne/maksymalne kwoty, które mogę jednorazowo wpłacić? </strong> 
 <p>
 Minimalna kwota wpłaty wynosi 10 € (lub ekwiwalent w Twojej walucie), a maksymalna kwota zależy od wybranej metody płatności.
 </p>
 </li>

 <li>
 <strong>
 Czy mogę ustawiać dzienne, tygodniowe i miesięczne limity wpłat?
 </strong>
 <p>
 Użytkownik może ustawiać dzienne, tygodniowe i miesięczne limity wpłat. Można je ustawić w podsekcji Mojego konta zatytułowanej „Odpowiedzialna gra”. Limity można zmieniać w każdej chwili w tym samym miejscu
 </p>
 </li>

 <li>
 <strong>Jak dokonuję wpłaty z mojego konta bankowego?</strong>
 <p>
 W sekcji Moje konto kliknij przycisk "Wpłać" i wybierz przelew bankowy. Możesz aktywować tę funkcję, klikając opcję „Wpłać”. Następnie postępuj zgodnie z instrukcjami na ekranie.
 </p>
 </li>

 <li>
 <strong>
 Czy istnieje tygodniowy limit wpłat na konto na stronie internetowej za pomocą karty kredytowej?
 </strong>
 <p>Maksymalna kwota wpłaty jednego dnia wynosi 5 000 €.</p>
 </li>

</ol>

