<ol>

 <li> <strong>Jak mogę zidentyfikować płatności lub wypłaty [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] na moim wyciągu bankowym lub z karty kredytowej?</strong> 
 <p>
 Wszystkim wpłatom i wypłatom&nbsp;, które pojawiają się na wyciągu z Twojej karty kredytowej lub konta bankowego będzie zawsze towarzyszył deskryptor, w którym [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] będą one wymienione. Umożliwi Ci to kontrolę wpłat na stronę i wypłat ze strony.
 </p>
 </li>

 <li> <strong>Czy mogę skorzystać z więcej niż jednej metody płatności?</strong>
 <p>*Tak, ale potrzebujemy wówczas dokumentacji potwierdzającej zmiany w metodach finansowych.
 </p>
 </li>

 <li>
 <strong>Czy akceptujecie karty z mojego kraju?</strong>
 <p>*Akceptujemy karty z wszystkich krajów poza Stanami Zjednoczonymi, Turcją, Francją, Wielką Brytanią i Hiszpanią.
 </p>
 </li>

 <li>
 <strong>Dlaczego Wasza strona internetowa nie akceptuje mojej karty?</strong>
 <p>
 Niektórzy wystawcy kart kredytowych zachowują zasadę, która uniemożliwia dokonywanie bezpośrednich wpłat na stronach gier internetowych. Użytkownik może skontaktować się z wystawcą karty celem omówienia tej kwestii lub zamiast tego spróbować jednej z opcji wpłat  e-portfela.&nbsp; Dalszej pomocy udzieli [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] dział obsługi klienta
 </p>
 </li>

 <li>
 <strong>Co mam zrobić, jeśli wpłata z mojej karty zostanie odrzucona lub anulowana?</strong>
 <p>
 Czasami, podczas próby dokonania wpłaty po raz drugi przed zatwierdzeniem pierwszej wpłaty, może dojść do przekroczenia dziennego lub tygodniowego limitu tygodniowego płatności, co może spowodować problemy. Najczęstszą przyczyną jest nieprawidłowe wypełnienie pól w formularzu zapytania. Skontaktuj się [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] z działem obsługi klienta, aby to skorygować. Poza tym niektórzy wystawcy kart kredytowych zachowują zasadę, która uniemożliwia dokonywanie bezpośrednich wpłat na stronach gier internetowych. Użytkownik może skontaktować się z wystawcą karty celem omówienia tej kwestii lub zamiast tego spróbować jednej z opcji wpłat e-portfela.
 </p>
 </li>

 <li>
 <strong>Czy mogę użyć swojej karty, żeby dokonać wpłaty na konto znajomego?</strong>
 <p>
 Nie można tego zrobić. Taka wpłata jest traktowana jako transakcja trzeciej strony i jako taka nie jest w żadnym razie dozwolona.
 </p>
 </li>

