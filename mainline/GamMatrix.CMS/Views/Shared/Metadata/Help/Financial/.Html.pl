 <li>
 <strong>Czy mogę przelewać pieniądze między kontami?</strong>
 <p>
 Nie można przelewać pieniędzy pomiędzy kontami osobistymi, ale można przesłać je między Kasynem, Zakładami sportowymi i Pokerem w obrębie własnego konta osobistego.
 </p>
 </li>

 <li>
 <strong>Czym jest IBAN?</strong>
 <p>
 IBAN to międzynarodowy numer rachunku bankowego. IBAN jest wykorzystywany w transakcjach przelewów międzynarodowych i ułatwia wypłaty wygranych.
 </p>
 </li>

 <li>
 <strong>Czym jest kod CVC2?</strong>
 <p>
 CVC2 to akronim od Card Verification Code (kodu weryfikacyjnego karty). Kod wymagany jest ze względów bezpieczeństwa podczas dokonywania międzynarodowych zakupów telefonicznych i płatności internetowych kartą kredytową. Składa się z trzech cyfr i znajduje się z tyłu karty kredytowej.
 </p>
 </li>

