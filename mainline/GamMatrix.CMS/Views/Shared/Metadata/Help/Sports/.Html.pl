<ol>

 <li> <strong>Jakie są minimalne i maksymalne dopuszczalne stawki zakładów?</strong>
 <p>
 Maksymalne stawki zakładów różnią się zależnie od okoliczności. Jeśli próbujesz obstawić zakład przekraczający maksymalny limit, otrzymasz wiadomość z maksymalną kwotą dozwoloną do obstawiania w danym wyborze. Nie stosujemy limitów minimalnej stawki zakładów.
 </p>
 </li>

 <li> <strong>Czym są pojedyncze i wielokrotne zakłady? </strong>
 <p>
 Pojedyncze zakłady oznaczają jeden wybór; wielokrotny zakład wymaga więcej niż jednego. Im więcej wyborów połączysz w wielokrotny zakład (np. wszystkie drużyny gospodarzy w rundzie Premier League), tym więcej możesz wygrać &ndash; ale oczywiście jest to o wiele trudniejsze!
 </p>
 </li>

 <li>
 <strong>Ile wyborów może zawierać jeden wielokrotny zakład?</strong>
 <p>
 Maksymalna liczba wyborów, na którą zezwalamy w wielokrotnym zakładzie wynosi 10.
 </p>
 </li>

 <li>
 <strong>Jak działa zakład typu handicap?</strong>
 <p>
 W celu uzyskania szczegółowych informacji na temat handicapów 1x2 z handicapem i handicapy azjatyckie (1x2 with Handicap oraz Asian Handicaps), przejdź do sekcji Opis zakładów tutaj.
 </p>
 </li>

 <li>
 <strong>Co się dzieje, jeśli mecz jest przesunięty?</strong>
 <p>
 Sprawdź zasady tych zakładów sportowych. Niektóre zakłady sportowe wymagają, aby mecz odbył się tego samego dnia. Obstawianie w innych dyscyplinach sportowych może pozostać otwarte do chwili zakończenia meczu lub jego oficjalnego porzucenia.
 </p>
 </li>

 <li>
 <strong>Dlaczego mój wygrany zakład nie został jeszcze wypłacony?</strong>
 <p>
 [Metadata:htmlencode(/Metadata/Settings.Operator_Display wymaga ogłoszenia oficjalnych wyników zanim rynki zostaną rozliczone. Czasem zasoby internetowe, takie jak strony wyników na żywo mogą nie działać prawidłowo, wówczas rozliczenie może zostać opóźnione do chwili potwierdzenia wyników.
 </p>
 </li>

 <li>
 <strong>Zmieniłem zdanie, czy mogę anulować swój zakład?</strong>
 <p>
 Nie. Po potwierdzeniu zakładu i zaakceptowaniu go przez nasz serwer, nie można już go anulować.
 </p>
 </li>

 <li>
 <strong>Gdzie mogę znaleźć listę moich zakładów?</strong>
 <p>
 Historię obstawiania i transakcji można znaleźć w sekcji „Moje konto zakładów sportowych”, po zalogowaniu się na [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] stronie internetowej.
 </p>
 </li>

 <li>
 <strong>Dlaczego wygrane są obliczane inaczej w przypadku zakładów dotyczących Wielkiej Brytanii?</strong>
 <p>
 Do obliczania wygranych wykorzystujemy kursy dziesiętne (europejskie), ponieważ są najdokładniejsze. Kursy angielskie i amerykańskie podawane są jedynie dla orientacji, ponieważ niektóre kursy dziesiętne nie przekładają się na proste ułamki.
 </p>
 </li>

 <li>
 <strong>Obstawianie zakładów coraz bardziej mnie uzależnia. Co mogę zrobić?</strong>
 <p>
 Obstawianie na [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] dla każdego powinno być przyjemnym sposobem na spędzanie czasu, ale w przypadku niektórych osób może być uzależniające i powodować problemy. Informacje dotyczące uzależnień hazardowych można znaleźć w sekcji Odpowiedzialna gry.
 </p>
 </li>

</ol>
