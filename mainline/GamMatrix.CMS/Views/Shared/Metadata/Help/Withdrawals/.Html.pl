<ol>

 <li> <strong>Jak dokonuję wypłaty z mojego konta na stronie internetowej?</strong>
 <p>
 Możesz poprosić o wypłatę w każdej&nbsp; chwili&nbsp;, o ile spełniasz wszystkie wymagania i zostałeś&nbsp; upoważniony do wypłat w naszym systemie. Możesz dokonywać wypłat tak często, jak chcesz, ale w trosce o przestrzeganie przepisów zapobiegających praniu brudnych pieniędzy i oszustwom finansowym [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] nie możemy zezwolić na wypłaty z Twojego [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)]&nbsp; konta przez 24 godziny po dokonaniu wpłaty. Minimalna&nbsp; kwota wypłaty wynosi 10 euro.
 </p>
 </li>

 <li> <strong>Czy istnieją limity kwot wypłaty?</strong>
 <p>
 Minimalna kwota wypłaty wynosi 10,00 € lub ekwiwalent w Twojej walucie. ** Maksymalna kwota wypłaty wynosi 5 000,00 € dziennie lub ekwiwalent w Twojej walucie.
 </p>
 </li>

 <li>
 <strong>Czy mogę anulować żądanie wypłaty?</strong>
 <p>Musisz skontaktować się z [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] z działem obsługi klienta, a wypłata zostanie wstrzymana, jeśli jeszcze nie została przetworzona.
 </p>
 </li>

 <li>
 <strong>Czy mogę wypłacić swoje wygrane na swoją kartę kredytową/debetową?</strong>
 <p>Tak, możesz, ale pod warunkiem, że jest to sama karta, której użyto do oryginalnej wpłaty, a sama karta umożliwia&nbsp; taką&nbsp; wypłatę.
 </p>
 </li>

 <li>
 <strong>Użyłem swojej karty kredytowej/debetowej do dokonania wpłaty czy mogę zażądać płatności w jakiś inny sposób?</strong> 
 <p>
 Tak, możesz, ale możemy poprosić Cię o pełną dokumentację i szczegóły dotyczące nowo wybranej metody.
 </p>
 </li>

 <li>
 <strong>Dlaczego nie mogę wybrać metody dokonywania wypłat?</strong>
 <p>
 Wynika to z przepisów zapobiegających praniu pieniędzy, których przestrzegamy, jak również z troski o bezpieczeństwo wszystkich transakcji finansowych - wszystkie środki muszą wrócić tą samą drogą, jakiej użyto do dokonania wpłaty.
 </p>
 </li>

 <li>
 <strong>Czy pobieracie opłaty za wypłatę?&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong> 
 <p>
 Opłaty za wypłatę różnią się w zależności od wybranej metody wypłaty. Kliknij tutaj '
 <a href="/deposit">Metody płatności</a>
 aby zobaczyć informacje dotyczące opłat. Pamiętaj, że Twój bank może obciążyć Cię opłatą za swoje usługi.
 </p>
 </li>

 <li>
 <strong>Jak długo trwa przetwarzanie wypłat?</strong>
 <p>
 Może to się różnić w zależności od wybranej metody wypłaty. Kliknij tutaj '
 <a href="/deposit">Metody płatności</a>
 ' to see all of the withdrawal methods available to you and their processing times.
 </p>
 </li>

 <li>
 <strong>
 Why is it that a withdrawal to my card takes days while a deposit is immediate?
 </strong>
 <p>
 Posiadamy liczne mechanizmy kontrolne i sprawdzające, które stosowane są zanim pieniądze są wypłacane [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] a to opóźnia wypłaty o około 12 godzin. Takie działania wynikają z naszego stałego zobowiązania, aby środki naszych klientów były zawsze bezpieczne. Wszelkie inne opóźnienia są spowodowane przez ograniczenia nałożone przez dostawców płatności.
 </p>
 </li>

 <li>
 <strong>
 Co się dzieje, jeśli karta, z której wypłaciłem pieniądze, straciła ważność lub została anulowana?
 </strong>
 <p>
 Musisz informować nas o wszelkich zmianach&nbsp; karty i dostarczyć dowód wygaśnięcia karty lub przyczynę zmiany.&nbsp; Każdą nową kartę należy zarejestrować u nas na swoim [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] koncie.
 </p>
 </li>

 <li>
 <strong>
 Czy mogę dokonać wypłaty z mojego konta i wysłać pieniądze komuś innemu?
 </strong>
 <p>
 Nie, traktuje się to jako transakcję trzeciej strony, a taka nie jest w żadnym razie dozwolona.
 </p>
 </li>

 <li>
 <strong>
 Czym jest  [Metadata:htmlencode(/Metadata/Settings.Operator_Display polityka wypłat?
 </strong>
 <p>
 Twoje konto musi być uruchomione i mieć minimalną kwotę pieniędzy wymaganą w celu wypłaty. Niektóre ograniczenia mogą obowiązywać graczy uczestniczących w [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] promotions.
 </p>
 </li>

</ol>

