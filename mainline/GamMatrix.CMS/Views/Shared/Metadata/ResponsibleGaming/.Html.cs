<p><strong> Pozastavení přístupu</strong> 
	<br /> 
	<br /> Pokud potřebujete přestávku od hraní, nabízíme vám možnost <a href="/SelfExclusion"> pozastavení přístupu </a> , které si můžete sami aktivovat v sekci „Můj Účet“ nebo kontaktujte Zákaznický servis. Pozastavení přístupu znamená, že po dobu minimálně 7 dní bude váš účet nepřístupný, a nebude možné ho za žádných podmínek v této době znovu aktivovat. Toto je hlavní změna oproti běžné žádosti o pozastavení či uzavření účtu. Po uplynutí lhůty pro pozastavení je nutné písemně sepsat žádost o případném obnovení účtu. 
	<br /> 
	<br />Stránka [Metadata:htmlencode(/Metadata/Settings.Operator_DisplayName)] nabízí možnost zablokovat přístup v rámci jednotlivých produktů (Sázky na zápasy, Kasino, Hry a Poker) přes sekci „Můj Účet“ nebo přes Zákaznický servis. 
	<br /> 
	<br /> Pokud zvažujete pozastavit svůj přístup, prosím nezapomeňte kontaktovat všechny herní společnosti, u nichž máte účet a pozastavte přístup i u nich. Dále doporučujeme zvážit instalaci softwaru, který by blokoval přístup na stránky herních společností. Více informací v sekci Nastavení filtrace ve spodní částí této stránky.</p>
