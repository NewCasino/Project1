<p><strong> Hraní pod kontrolou </strong> 
	<br /> 
	<br /> Hraní by mělo být chápáno jako zábava, nikoliv jako prostředek ke zvýšení příjmů. Zatímco většina populace dokáže hrát pouze v rámci volného času a utratit pouze tolik peněz, kolik si může dovolit vsadit, pro určité lidi může toto být problém. Abyste dokázali udržet vaše hráčství pod kontrolou, připomínáme vám několik zásad, kterými byste se měli vždy řídit:<br /> <br />* Hrajte s mírou a pouze pro zábavu. Neberte hraní jako zdroj obživy. <br />* Vyvarujte se snaze po vyrovnání ztrát. Vždy je tu ještě zítřek. <br />* Hrajte jen tehdy, pokud máte na zaplacení.<br />* Sledujte, kolik času hrajete a kolik peněz jste utratili.<br />* Pokud potřebujete pauzu od hraní, kontaktujte Zákaznický servis a požádejte o pozastavení přístupu k našim produktům.<br /> <br /> Pokud si potřebuje o svých obavách o závislosti na gamblerství s někým promluvit, prosím kontaktujte jednu z organizací, jejichž seznam je uveden níže. </p>
<br />
<p><strong> Máte problém?</strong> 
	<br /> 
	<br /> Pokud se obáváte, že hraní může mít negativní dopad na váš osobní život, nebo na život vašich blízkých, zodpovězení následujících otázek vám může pomoci.
	<br /> 
	<br />1. Zabraňuje vám hraní chodit do školy či do práce?
	<br />2. Hrajete, protože se nudíte a chcete nějak zaplnit čas?
	<br />3. Hrajete o samotě a dlouhé hodiny?
	<br />4. Kritizoval vás již někdo kvůli hraní?
	<br />5. Přestali jste se kvůli hraní zajímat o rodinu, přátele a koníčky?
	<br />6. Lhali jste někdy o čase stráveném hraním nebo o výši částky, kterou jste prohráli? 
	<br />7. Lhali jste, ukradli jste či jste si něco půjčili, jen abyste mohli hrát dál?
	<br />8. Je vám za těžko utratit peníze určené na hraní za něco jiného?
	<br />9. Hrajete, dokud neprohrajete všechny peníze?
	<br />10. Když prohrajete, máte pocit, že musíte hrát dál a vyhrát prohrané peníze zpět co nejdříve?
	<br />11. Pokud hrajete a dojdou vám peníze, cítíte se mizerně a máte nutkání hrát, co nejdříve to bude možné?

	<br />12. Vyvolávají ve vás hádky, frustrace a zklamání touhu si zahrát?
	<br />13. Vyvolává ve vás hraní pocit deprese a myšlenky na sebevraždu?
	<br /> <br /> Na čím více otázek jste odpověděli Ano, tím je pravděpodobnější, že máte problémy s hraním. Prosím promluvte si s někým, kdo vám může poradit a podpořit vás, kontaktujte nějakou z organizací v níže uvedené v sekci Poradny pro závislosti na gamblerství. </p>
