<p> Tyto informace shromažďujeme, když se zaregistrujete a používáte naše herní produkty, vznášíte dotazy, registrujte se pro odběr speciálních nabídek a dalšíchh služeb, nebo když odpovídáte na komunikaci z naší strany, jako jsou, ale ne výlučně, průzkumy a dotazníky. Informace, které shromažďujeme mohou obsahovat osobní a bankovní údaje a jakékoli další informace, které nám sdělíte.</p> 
</li>
<li>
<strong> Jak jsou tyto informace je zpracovány</strong>
<p> Informace, které nám poskytnete budou zpracovány z několika důvodů, mezi než patří poskytování herních služeb, zpracování vašich dotazů, poskytování Zákaznického servisu, z marketingových účelů, abychom vás upozornili na možné změny na webových stránkách, abychom splnili to, co se od nás očekává v rámci platných zákonů v zemi pro níž máme paltnou licenci, či z jiného důvodu, který nás opravňuje k tomu, po vás požadovat osobní údaje.</p>
</li>
<li>
<strong>Komu prozradíme vaše osobní data? </strong>
<p>
Vaše osobní údaje mohou být zveřejněny:<br />
a)  V rámci Společnosti. V tomto případě se ujišťujeme, aby přístup měli jen k tomu pověření zaměstnanci;<br />
b)  Třetí stranám, které používají naší značku a poskytují vám herní služby. <br />
c)  Třetí stranám, které společnosti poskytují služby, která nám umožňují zlepšit poskytování služeb pro vás, např. Platební služby či ověřovače informací;<br />
d)  Konzultantům, jako např. auditorům, kteří poskytují své služby v rámci společnosti a kteří mají za cíl zlepšit kvalitu poskytovaných služeb a zefektivnit chod psolečnosti. <br />
e)  Potenciálním investorům či kupcům jakékoliv podstatné části společnosti. <br />
f)  Vládním autoritám či jiným institucím, ač se toto děje pouze velice výjimečně a dle zákona. <br />
Každá třetí strana, které jsme se rozhodli prozradit vaše osobní údaje, bude smluvně vázána, v souladu se zákony na ochranu dat a zákony na ochranu soukromí a Podmínkami ochrany osobních údajů. </p>
