<strong> Ochrana osobních údajů</strong>
<p> Používáním a / nebo navštívením jakékoliv části webových stránek [Metadata: hodnota (/Metadata/Settings.Operator_DisplayName)] (<b> "Webové stránky" </ b>); nebo založením účtu na webových stránkách, souhlasíte s pravidly ochrany osobních údajů. Při použití některého z produktů Webových stránek budete požádáni, aby jste nám poskytli pravdivé, aktuální a přesné osobní údaje, které nám umožní ověření vaší totožnosti.</p>
<p> i) Výslovně dáváte souhlas se zpracováním vašich osobních dat v souladu se zákony na ochranu soukromí v příslušných jurisdikcích.</p>
<p> ii) 
